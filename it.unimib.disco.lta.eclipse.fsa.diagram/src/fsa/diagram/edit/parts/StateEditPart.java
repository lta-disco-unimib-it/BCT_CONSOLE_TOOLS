		public void updateFace() {

			State state = (State) ((Node) StateEditPart.this.getModel()).getElement(); // get the current State

			if (state.isFinal() == true ) { // if state is final, make a red edge

				setLineWidth(4);
				setForegroundColor(new Color(null, 255, 0, 0));
				setBackgroundColor(new Color(null, 255, 255, 255));

			}else{

				if (getLineWidth()==4){ // state was final, now must be return with normal edge
					setLineWidth(1);
					setForegroundColor(new Color(null, 1, 1, 1));
				}
				setBackgroundColor(new Color(null, 255, 255, 255));
			}

			this.fFigureStateNameFigure.setForegroundColor(ColorConstants.black);
			this.fFigureStateNameFigure.setBackgroundColor(ColorConstants.black);

			if (state.getFsa()!= null){

				if ( state.getName().equals(state.getFsa().getInitialState().getName())){// set appearance initial state
					setBackgroundColor(new Color(null, 1, 1, 1)); // make a black background

					this.fFigureStateNameFigure.setForegroundColor(ColorConstants.white);
					this.fFigureStateNameFigure.setBackgroundColor(ColorConstants.white);
				}

			}

			this.add(fFigureStateNameFigure); // add state
		}
